-- ---- objects/categories/event_types: choose your sharing model
-- Here: readable to authenticated users; writes locked down (common for lookup tables)
alter table public.categories enable row level security;
alter table public.objects enable row level security;
alter table public.event_types enable row level security;

create policy "read categories"
on public.categories for select
using (auth.role() = 'authenticated');

create policy "read objects"
on public.objects for select
using (auth.role() = 'authenticated');

create policy "read event types"
on public.event_types for select
using (auth.role() = 'authenticated');


-- ---- events: everyone in my group can read; inserts/updates restricted to same group
alter table public.events enable row level security;

create policy "read events in my group"
on public.events
for select
using (group_id = public.current_group_id());

create policy "insert events in my group (must be sender)"
on public.events
for insert
with check (
  group_id = public.current_group_id()
  and e_from = auth.uid()
);

create policy "update events in my group (sender only)"
on public.events
for update
using (
  group_id = public.current_group_id()
  and e_from = auth.uid()
)
with check (
  group_id = public.current_group_id()
  and e_from = auth.uid()
);

create policy "delete events in my group (sender only)"
on public.events
for delete
using (
  group_id = public.current_group_id()
  and e_from = auth.uid()
);
